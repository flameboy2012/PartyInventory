// Generates src/lib/api/routes.ts — an allowlist of API routes (method + path
// regex) derived from the committed OpenAPI spec. The BFF proxy uses it to
// validate incoming requests against the real API surface before forwarding.
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const specPath = resolve(here, "../../openapi/v1.json");
const outPath = resolve(here, "../src/lib/api/routes.ts");

const HTTP_METHODS = ["get", "post", "put", "patch", "delete"];

const spec = JSON.parse(readFileSync(specPath, "utf8"));

/** Turn an OpenAPI path template into an anchored regex source. */
function toRegexSource(template) {
  const body = template
    .split(/(\{[^}]+\})/)
    .map((part) =>
      /^\{[^}]+\}$/.test(part)
        ? "[^/]+" // path parameter — match a single segment
        : part.replace(/[.*+?^${}()|[\]\\]/g, "\\$&"),
    )
    .join("");
  return `^${body}$`;
}

const entries = [];
for (const [template, pathItem] of Object.entries(spec.paths ?? {})) {
  const source = toRegexSource(template);
  for (const method of Object.keys(pathItem)) {
    if (HTTP_METHODS.includes(method)) {
      entries.push({ method: method.toUpperCase(), source });
    }
  }
}
entries.sort((a, b) => a.source.localeCompare(b.source) || a.method.localeCompare(b.method));

const lines = entries
  .map((e) => `  { method: ${JSON.stringify(e.method)}, source: ${JSON.stringify(e.source)} },`)
  .join("\n");

const content = `// AUTO-GENERATED by scripts/generate-api-routes.mjs — do not edit by hand.
// Run \`npm run gen:api\` to regenerate from openapi/v1.json.

const ENTRIES: { method: string; source: string }[] = [
${lines}
];

const API_ROUTES = ENTRIES.map((entry) => ({
  method: entry.method,
  pattern: new RegExp(entry.source),
}));

/** Whether the given method + path matches a known API route. */
export function isAllowedRoute(method: string, path: string): boolean {
  const upper = method.toUpperCase();
  return API_ROUTES.some((route) => route.method === upper && route.pattern.test(path));
}
`;

writeFileSync(outPath, content);
console.log(`Wrote ${entries.length} routes to ${outPath}`);
